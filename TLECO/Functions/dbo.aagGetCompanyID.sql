 CREATE   FUNCTION [dbo].[aagGetCompanyID] ( ) RETURNS INTEGER AS  BEGIN DECLARE @CMPANYID INTEGER SELECT @CMPANYID = DYNAMICS..SY01500.CMPANYID FROM DYNAMICS..SY01500 INNER JOIN  DYNAMICS..ACTIVITY ON DYNAMICS..SY01500.CMPNYNAM = DYNAMICS..ACTIVITY.CMPNYNAM WHERE DYNAMICS..ACTIVITY.USERID = SYSTEM_USER AND DYNAMICS..SY01500.INTERID = DB_NAME() RETURN ISNULL(@CMPANYID, 0) END     
GO
