-- RM Customer Address on table RM00102.  
 
CREATE VIEW [dbo].[ReqRMCustomerAddress] AS 
select  
	RM00101.CUSTNMBR, 
	RM00102.ADRSCODE, 
	RM00101.CUSTNAME, 
	RM00102.SLPRSNID, 
	RM00102.UPSZONE, 
	RM00102.SHIPMTHD, 
	RM00102.TAXSCHID, 
	RM00102.CNTCPRSN, 
	RM00102.ADDRESS1, 
	RM00102.ADDRESS2, 
	RM00102.ADDRESS3, 
	RM00102.COUNTRY, 
	RM00102.CITY, 
	RM00102.STATE, 
	RM00102.ZIP, 
	RM00102.PHONE1, 
	RM00102.PHONE2, 
	RM00102.PHONE3, 
	RM00102.FAX, 
	RM00102.CCode, 
	RM00102.LOCNCODE, 
	RM00102.SALSTERR, 
	RM00102.USERDEF1, 
	RM00102.USERDEF2, 
	SY01200.INET1, 
	RM00102.CREATDDT, 
	RM00102.DEX_ROW_TS, 
	RM00102.DEX_ROW_ID 
from  
	RM00102 left outer join SY01200 on ( 
		RM00102.CUSTNMBR = SY01200.Master_ID AND  
		RM00102.ADRSCODE = SY01200.ADRSCODE AND  
		SY01200.Master_Type = 'CUS') 
	JOIN RM00101 ON RM00102.CUSTNMBR = RM00101.CUSTNMBR 
 
GO
