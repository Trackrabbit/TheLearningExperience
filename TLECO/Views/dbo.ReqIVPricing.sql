CREATE VIEW [dbo].[ReqIVPricing] AS 
select  
	IV00107.ITEMNMBR, 
	PRICMTHD, 
	UOFM, 
	IV00107.PRCLEVEL, 
	CURNCYID, 
	isnull((select ISOCURRC from DYNAMICS..MC40200 where CURNCYID = IV00107.CURNCYID),(select ISOCURRC from DYNAMICS..MC40200 where CURNCYID in (select FUNLCURR from MC40000)))  "ISOCURRC",  
	ROUNDHOW, 
	ROUNDTO, 
	DECPLCUR, 
	RNDGAMNT, 
	UMSLSOPT, 
	IV00107.DEX_ROW_TS, 
	IV00107.DEX_ROW_ID 
 
from  
	IV00107 join IV00101 on (IV00107.ITEMNMBR = IV00101.ITEMNMBR) 
GO
