-- GL Posting Account View  
 
CREATE VIEW [dbo].[ReqGLPostingAcct] AS 
 
select  
	ACTNUMST, 
	ACTALIAS, 
	ACTDESCR, 
	PSTNGTYP, 
	ACCATDSC, 
	ACTIVE, 
	TPCLBLNC, 
	USERDEF1, 
	USERDEF2, 
	PostSlsIn, 
	PostIvIn, 
	PostPurchIn, 
	PostPRIn, 
	ACCTENTR, 
	USRDEFS1, 
	USRDEFS2, 
	CURNCYID, 
	REVALUE, 
	REVLUHOW, 
	Post_Results_To, 
	GL00100.ACTINDX, 
	ACCTTYPE, 
	CREATDDT, 
	GL00100.DEX_ROW_TS, 
	GL00100.DEX_ROW_ID 
from  
	GL00100 join GL00105 on (GL00100.ACTINDX = GL00105.ACTINDX) 
	     	left outer join MC00200 on (GL00100.ACTINDX = MC00200.ACTINDX and MC00200.CURNCYID = '') 
		left outer join GL00102 on (GL00100.ACCATNUM = GL00102.ACCATNUM) 
where 
	ACCTTYPE = 1 
GO
