 Create Procedure [dbo].[SVC_ISC_Call_Lines]  (  @CallType smallint=2,  @CallNumber char(15)  ) As SELECT SVC00203.LNITMSEQ,   CASE WHEN SVC00203.LINITMTYP = 'P' THEN 'Part'  WHEN SVC00203.LINITMTYP = 'L' THEN 'Labor'  ELSE 'Misc'  END 'LNITMTYP',   SVC00203.ITEMNMBR,  SVC00203.ITEMDESC,   SVC00203.UOFM,   SVC00203.QTYORDER,   SVC00203.XTNDPRCE,   SVC00913.STSDESCR  FROM SVC00203 LEFT OUTER JOIN   SVC00913 ON (SVC00203.SRVSTAT = SVC00913.SRVSTAT) WHERE SVC00203.SRVRECTYPE = @CallType AND SVC00203.CALLNBR = @CallNumber ORDER BY SVC00203.LINITMTYP, SVC00203.LNITMSEQ  return    
GO
GRANT EXECUTE ON  [dbo].[SVC_ISC_Call_Lines] TO [DYNGRP]
GO
