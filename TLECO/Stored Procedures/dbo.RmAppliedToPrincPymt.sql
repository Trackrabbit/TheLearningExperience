 create procedure [dbo].[RmAppliedToPrincPymt]  @iApplyFromDocNum varchar(25),  @oExists int OUTPUT  AS  DECLARE  @lERRORSTATUS int  IF EXISTS(SELECT   *  FROM  RM20201 JOIN RM20101  ON  (RM20201.APTODCNM = RM20101.DOCNUMBR and RM20201.APTODCTY = RM20101.RMDTYPAL)  WHERE  RM20201.APFRDCNM = @iApplyFromDocNum  AND  RM20101.CSHRCTYP = 1 and RM20201.APFRDCTY in (7,8,9)) BEGIN  set @oExists = 1 END ELSE BEGIN  set @oExists = 0 END  RETURN (@lERRORSTATUS)    
