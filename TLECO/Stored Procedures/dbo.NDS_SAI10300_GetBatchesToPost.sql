Create PROCEDURE [dbo].[NDS_SAI10300_GetBatchesToPost]
 AS 
 BEGIN 
   SELECT DISTINCT BACHNUMB
     FROM SY00500
     NOLOCK WHERE BACHNUMB LIKE 'XFR%' 
              and BCHSOURC = 'Sales Entry' 
              and NUMOFTRX > 0 
 END 
GO
