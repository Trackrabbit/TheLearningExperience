 CREATE Procedure [dbo].[SVC_ISC_SerialListByFilter]  (  @CUSTNMBR char(15),  @ADDRESSID char(15),  @Filter varchar(255)  ) As SELECT DISTINCT  EQUIPID, SERLNMBR ,IV00101.ITEMNMBR ,ITEMDESC , WARREND, SLRWEND FROM SVC00300 INNER JOIN  IV00101 ON (SVC00300.ITEMNMBR = IV00101.ITEMNMBR)  WHERE( SVC00300.CUSTNMBR = @CUSTNMBR) and (SVC00300.ADRSCODE = @ADDRESSID) and (SVC00300.ITEMNMBR like @Filter  or   UPPER(IV00101.ITEMDESC) like @Filter) order by IV00101.ITEMNMBR, SERLNMBR    
GO
