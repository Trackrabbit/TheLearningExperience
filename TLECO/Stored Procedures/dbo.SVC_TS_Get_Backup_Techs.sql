 CREATE PROCEDURE [dbo].[SVC_TS_Get_Backup_Techs]  (@TECHID char(11),  @STARTDATE datetime,   @STARTTIME datetime,   @ENDDATE datetime,     @ENDTIME datetime      ) AS select  SVC00100.TECHID, SVC00100.NAME from SVC00100  where  SVC00100.TECHID <> @TECHID and not SVC00100.TECHID in  (select SVC00101.TECHID from SVC00101 inner join   SVC00905 ON (SVC00101.TECHSTAT = SVC00905.TECHSTAT) where   SVC00101.STRTDATE >= @STARTDATE + ' 00:00:00' and SVC00101.STRTTIME >= '01/01/1900 ' + @STARTTIME and  SVC00101.ENDDATE <= @ENDDATE + ' 00:00:00' and SVC00101.ENDTME <= '01/01/1900 ' + @ENDTIME and  SVC00905.Tech_Available = 0)   
GO
GRANT EXECUTE ON  [dbo].[SVC_TS_Get_Backup_Techs] TO [DYNGRP]
GO
