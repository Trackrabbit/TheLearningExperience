 CREATE PROC [dbo].[zDP_HRPREASNN_1] (@BS int, @CHANGEREASON_I char(31), @CHANGEREASON_I_RS char(31), @CHANGEREASON_I_RE char(31)) AS  set nocount on IF @CHANGEREASON_I_RS IS NULL BEGIN SELECT TOP 25  CHANGEREASON_I, DEX_ROW_ID FROM .HRPREASN WHERE ( CHANGEREASON_I > @CHANGEREASON_I ) ORDER BY CHANGEREASON_I ASC END ELSE IF @CHANGEREASON_I_RS = @CHANGEREASON_I_RE BEGIN SELECT TOP 25  CHANGEREASON_I, DEX_ROW_ID FROM .HRPREASN WHERE CHANGEREASON_I = @CHANGEREASON_I_RS AND ( CHANGEREASON_I > @CHANGEREASON_I ) ORDER BY CHANGEREASON_I ASC END ELSE BEGIN SELECT TOP 25  CHANGEREASON_I, DEX_ROW_ID FROM .HRPREASN WHERE CHANGEREASON_I BETWEEN @CHANGEREASON_I_RS AND @CHANGEREASON_I_RE AND ( CHANGEREASON_I > @CHANGEREASON_I ) ORDER BY CHANGEREASON_I ASC END set nocount off    
