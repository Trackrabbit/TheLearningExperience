 CREATE PROC [dbo].[zDP_DEFLTRN_1] (@BS int, @RPRTNAME char(31), @RPRTNAME_RS char(31), @RPRTNAME_RE char(31)) AS  set nocount on IF @RPRTNAME_RS IS NULL BEGIN SELECT TOP 25  RPRTNAME, DEX_ROW_ID, TXTFIELD FROM .DEFLTR WHERE ( RPRTNAME > @RPRTNAME ) ORDER BY RPRTNAME ASC END ELSE IF @RPRTNAME_RS = @RPRTNAME_RE BEGIN SELECT TOP 25  RPRTNAME, DEX_ROW_ID, TXTFIELD FROM .DEFLTR WHERE RPRTNAME = @RPRTNAME_RS AND ( RPRTNAME > @RPRTNAME ) ORDER BY RPRTNAME ASC END ELSE BEGIN SELECT TOP 25  RPRTNAME, DEX_ROW_ID, TXTFIELD FROM .DEFLTR WHERE RPRTNAME BETWEEN @RPRTNAME_RS AND @RPRTNAME_RE AND ( RPRTNAME > @RPRTNAME ) ORDER BY RPRTNAME ASC END set nocount off    
GO
